# KnowledgeDB  — Unified knowledge database (JSON-backed, singleton)
# (previously modules/knowledge_db.py)
# ══════════════════════════════════════════════════════════════════════════════

_kdb_log = logging.getLogger("KnowledgeDB")


class KnowledgeDB:
    """
    Unified Knowledge Database for Niblit.

    Combines MemoryManager-style features with a richer fact/queue/acquired-data
    model used by the Autonomous Learning Engine (ALE).  Singleton pattern.
    """

    _instance = None  # singleton

    def __new__(cls, *args, **kwargs):
        if cls._instance is None:
            cls._instance = super(KnowledgeDB, cls).__new__(cls)
        return cls._instance

    def __init__(
        self,
        path: Optional[str] = None,
        autosave_interval: int = 60,
        dump_interval: int = 300,
    ) -> None:
        if getattr(self, "_initialized", False):
            return
        self._initialized = True

        self.path = path or _writable_path("niblit_memory.json", "NIBLIT_MEMORY_PATH")
        self.autosave_interval = autosave_interval
        self.dump_interval = dump_interval

        self.lock = threading.RLock()
        self.data: Dict[str, Any] = {
            "facts": [],
            "interactions": [],
            "learning_log": [],
            "learning_queue": [],
            "preferences": {"mood": "neutral", "verbosity": "medium"},
            "events": [],
            "meta": {},
        }

        self._load()

        # KnowledgeDB no longer starts its own autosave/dump threads.
        # NiblitMemory's single autosave loop covers KnowledgeDB too.
        _kdb_log.info("KnowledgeDB initialized (save/dump managed by NiblitMemory)")

    # ── internal load / save ──────────────────────────────────────────────────

    def _load(self) -> None:
        try:
            if os.path.exists(self.path):
                with open(self.path, "r", encoding="utf-8") as f:
                    loaded = json.load(f)
                    self.data.update(loaded)
                    _kdb_log.info("KnowledgeDB loaded from %s", self.path)
        except Exception as e:
            _kdb_log.error("Failed to load KnowledgeDB: %s", e)
            self._save(blocking=False)

    def _save(self, blocking: bool = True) -> None:
        def _do_save() -> None:
            try:
                with self.lock:
                    with open(self.path, "w", encoding="utf-8") as f:
                        json.dump(self.data, f, indent=4, ensure_ascii=False)
                    _kdb_log.debug("KnowledgeDB saved")
            except Exception as e:
                _kdb_log.error("KnowledgeDB save failed: %s", e)

        if blocking:
            _do_save()
        else:
            threading.Thread(target=_do_save, daemon=True, name="KnowledgeDB-SaveBG").start()

    # ── generic get / set ─────────────────────────────────────────────────────

    def set(self, key: str, value: Any) -> None:
        with self.lock:
            self.data[key] = value
        self._save(blocking=False)

    def get(self, key: str, default: Any = None) -> Any:
        with self.lock:
            return self.data.get(key, default)

    # ── events ────────────────────────────────────────────────────────────────

    def log_event(self, text: str) -> None:
        with self.lock:
            self.data.setdefault("events", [])
            self.data["events"].append({
                "time": datetime.utcnow().isoformat(),
                "event": text,
            })
        _kdb_log.info("[Event] %s", text)
        self._save(blocking=False)

    def get_events(self) -> List[Dict[str, Any]]:
        with self.lock:
            return list(self.data.get("events", []))

    # ── interactions / learning ───────────────────────────────────────────────

    def add_interaction(
        self,
        user_input: Any = None,
        response: Any = None,
        source: str = "unknown",
        data: Any = None,
    ) -> None:
        # Reject "No data found" placeholder content — it is not real knowledge.
        if _is_no_data_placeholder(user_input) or _is_no_data_placeholder(response):
            return
        interaction = self._canonicalize(
            raw_input=user_input, response=response, source=source, data=data
        )
        self.store_learning(interaction)
        with self.lock:
            self.data.setdefault("interactions", [])
            self.data["interactions"].append(interaction)
        self.log_event(f"Interaction stored from {source}")
        self._save(blocking=False)

    def store_learning(self, entry: Any) -> None:
        # Skip entries whose text fields contain "No data found" placeholders.
        if isinstance(entry, dict):
            for _field in ("input", "response", "value"):
                if _is_no_data_placeholder(entry.get(_field)):
                    return
        with self.lock:
            self.data.setdefault("learning_log", [])
            self.data["learning_log"].append(entry)
        self._save(blocking=False)

    def get_learning_log(self) -> List[Any]:
        with self.lock:
            return list(self.data.get("learning_log", []))

    def add_entry(self, key: str, value: Any) -> None:
        """LocalDB-compatible shim so SLSA and other modules can call add_entry on KnowledgeDB."""
        with self.lock:
            self.data.setdefault("interactions", [])
            self.data["interactions"].append({
                "ts": time.time(),
                "key": key,
                "value": value,
            })
        self._save(blocking=False)

    # ── facts / queue ─────────────────────────────────────────────────────────

    def add_fact(self, key: str, value: Any, tags: Optional[List[str]] = None) -> None:
        # ── Knowledge filter guard (additive) ────────────────────────────────
        # Only persist genuine research/learning facts, not internal system
        # metadata such as routing events, loop ticks, or ALE step counters.
        try:
            from modules.knowledge_filter import get_knowledge_filter as _gkf
            _kf = _gkf()
            if not _kf.should_store(key, value, tags):
                return
            # Compress value for readability & disk efficiency
            if isinstance(value, str) and len(value) > 600:
                value = _kf.compress(value, max_len=600)
        except ImportError:
            pass  # filter not yet available — store as-is
        # ── Original storage logic ────────────────────────────────────────────
        with self.lock:
            self.data.setdefault("facts", [])
            self.data["facts"].append({
                "key": key,
                "value": value,
                "tags": tags or [],
                "ts": int(time.time()),
            })
        self._save(blocking=False)

    def store_research(
        self,
        key: str,
        text: str,
        tags: Optional[List[str]] = None,
        source: str = "research",
    ) -> None:
        """Preferred entry-point for persisting research & learning content.

        Unlike ``add_fact()``, this method:
        * Always passes the knowledge filter (``research`` tag is whitelisted).
        * Summarizes long text before storage so the KB stays readable.
        * Includes ``source`` in the stored record for provenance.
        """
        tags = list(tags or [])
        if "research" not in tags:
            tags.append("research")
        try:
            from modules.knowledge_filter import get_knowledge_filter as _gkf
            summary = _gkf().summarize_for_storage(key, text, tags)
        except ImportError:
            summary = str(text)[:600]
        with self.lock:
            self.data.setdefault("facts", [])
            self.data["facts"].append({
                "key": key,
                "value": summary,
                "tags": tags,
                "source": source,
                "ts": int(time.time()),
            })
        self._save(blocking=False)

    def list_facts(self, limit: int = 100) -> List[Dict[str, Any]]:
        with self.lock:
            facts = list(self.data.get("facts", []))
        return sorted(facts, key=lambda x: x.get("ts", 0), reverse=True)[:limit]

    def queue_learning(self, topic: str) -> None:
        with self.lock:
            self.data.setdefault("learning_queue", [])
            # Skip if an identical topic is already pending to prevent duplicate
            # entries from piling up and causing an infinite research loop.
            already_queued = any(
                isinstance(item, dict)
                and item.get("topic") == topic
                and item.get("status") == "queued"
                for item in self.data["learning_queue"]
            )
            if already_queued:
                return
            # Prune old "processed" entries for this topic so the queue does not
            # grow without bound over long-running sessions.  A new list is built
            # (not in-place modification) and assigned back intentionally.
            pruned = [
                item for item in self.data["learning_queue"]
                if not (isinstance(item, dict) and item.get("topic") == topic
                        and item.get("status") == "processed")
            ]
            self.data["learning_queue"] = pruned
            self.data["learning_queue"].append({
                "topic": topic, "status": "queued", "ts": int(time.time()),
            })
        self._save(blocking=False)

    def get_learning_queue(self) -> List[Dict[str, Any]]:
        with self.lock:
            return list(self.data.get("learning_queue", []))

    def mark_learning_done(self, topic: str) -> None:
        with self.lock:
            for item in self.data.get("learning_queue", []):
                if isinstance(item, dict) and item.get("topic") == topic:
                    item["status"] = "processed"
        self._save(blocking=False)

    # ── preferences ───────────────────────────────────────────────────────────

    def store_preferences(self, prefs: Dict[str, Any]) -> None:
        with self.lock:
            self.data["preferences"] = prefs
        self._save(blocking=False)

    def get_preferences(self) -> Dict[str, Any]:
        with self.lock:
            return dict(self.data.get("preferences", {}))

    def get_personality(self) -> Dict[str, Any]:
        return self.get_preferences()

    # ── canonicalization ──────────────────────────────────────────────────────

    def _canonicalize(
        self,
        raw_input: Any = None,
        response: Any = None,
        source: str = "unknown",
        data: Any = None,
    ) -> Dict[str, Any]:
        now = datetime.utcnow().isoformat()
        record: Dict[str, Any] = {
            "time": now, "speaker": "unknown",
            "input": None, "response": response,
            "source": source, "data": data,
        }
        if isinstance(raw_input, dict):
            record["data"] = raw_input
            return record
        if isinstance(raw_input, str):
            txt = raw_input.strip()
            m = re.match(r"^(user|assistant|agent|system)\s*:\s*(.+)$", txt, re.I)
            if m:
                record["speaker"] = m.group(1).lower()
                record["input"] = m.group(2)
            else:
                record["speaker"] = "user"
                record["input"] = txt
        return record

    # ── recall / search ───────────────────────────────────────────────────────

    def search(self, query: str, limit: int = 5, max_results: Optional[int] = None) -> List[Any]:
        """Search through facts and learning log for matching entries."""
        if max_results is not None:
            limit = max_results
        if not query or not query.strip():
            return []
        query_words = set(query.lower().split())
        results: List[Any] = []
        seen: set = set()

        def _check(obj: Any) -> None:
            try:
                text = json.dumps(obj).lower()
                if any(w in text for w in query_words):
                    key = json.dumps(obj)
                    if key not in seen and len(results) < limit:
                        seen.add(key)
                        results.append(obj)
            except Exception:
                pass

        with self.lock:
            for fact in reversed(self.data.get("facts", [])):
                _check(fact)
            for entry in reversed(self.data.get("learning_log", [])):
                _check(entry)
            for interaction in reversed(self.data.get("interactions", [])):
                _check(interaction)

        return results[:limit]

    def recall(
        self,
        query: str,
        limit: int = 10,
        include_preferences: bool = True,
    ) -> List[Any]:
        """Recall information matching the query from facts, events, and learning log."""
        if not query:
            query = ""
        if not query.strip():
            return (self.list_facts(limit) + list(reversed(self.get_learning_log()))[:limit])[:limit]

        query_words = set(query.lower().split())
        results: List[Any] = []
        seen: set = set()

        def _matches(obj: Any) -> bool:
            try:
                return any(w in json.dumps(obj).lower() for w in query_words)
            except Exception:
                return False

        def _add(obj: Any) -> None:
            try:
                key = json.dumps(obj, sort_keys=True)
            except Exception:
                key = str(obj)
            if key not in seen and len(results) < limit:
                seen.add(key)
                results.append(obj)

        with self.lock:
            for fact in reversed(self.data.get("facts", [])):
                if _matches(fact):
                    _add(fact)
        for entry in reversed(self.get_learning_log()):
            if _matches(entry):
                _add(entry)
        with self.lock:
            for ev in reversed(self.data.get("events", [])):
                if _matches(ev):
                    _add(ev)
            for inter in reversed(self.data.get("interactions", [])):
                if _matches(inter):
                    _add(inter)
        if include_preferences:
            for k, v in self.get_preferences().items():
                if any(w in str(v).lower() for w in query_words):
                    _add({k: v})
        return results[:limit]

    # ── acquired data (ALE) ───────────────────────────────────────────────────

    def get_acquired_data(
        self,
        category: Optional[str] = None,
        limit: int = 50,
    ) -> List[Dict[str, Any]]:
        """Return structured data acquired by the Autonomous Learning Engine."""
        with self.lock:
            facts = list(self.data.get("facts", []))
        facts_sorted = sorted(facts, key=lambda x: x.get("ts", 0), reverse=True)
        if not category or category.lower() in ("all", ""):
            return facts_sorted[:limit]
        cat_lower = category.lower()
        filtered = [
            f for f in facts_sorted
            if any(cat_lower in str(t).lower() for t in f.get("tags", []))
            or cat_lower in str(f.get("key", "")).lower()
        ]
        return filtered[:limit]

    def get_knowledge_summary(self) -> str:
        """Return a human-readable summary of everything stored in the KnowledgeDB.

        Uses the KnowledgeFilter to present only genuine research/learning facts
        as clean bullet points, not raw system metadata or JSON blobs.
        """
        with self.lock:
            facts = list(self.data.get("facts", []))
            events = list(self.data.get("events", []))
            interactions = list(self.data.get("interactions", []))
            learning_log = list(self.data.get("learning_log", []))
            queue = list(self.data.get("learning_queue", []))

        pending_research = sum(
            1 for q in queue if isinstance(q, dict) and q.get("status") == "queued"
        )
        tag_counts: Dict[str, int] = {}
        for fact in facts:
            for tag in fact.get("tags", []):
                tag_counts[tag] = tag_counts.get(tag, 0) + 1

        ale_cats = {
            "research": 0, "code": 0, "compiled": 0,
            "reflection": 0, "software_study": 0, "autonomous": 0,
        }
        for tag, count in tag_counts.items():
            for cat in ale_cats:
                if cat in tag.lower():
                    ale_cats[cat] += count

        recents: Dict[str, str] = {}
        for fact in sorted(facts, key=lambda x: x.get("ts", 0), reverse=True):
            tags_lower = [str(t).lower() for t in fact.get("tags", [])]
            for cat in ale_cats:
                if cat not in recents and any(cat in t for t in tags_lower):
                    raw_val = fact.get("value", "")
                    # Extract a readable string from dict values instead of Python repr
                    if isinstance(raw_val, dict):
                        raw_val = (
                            raw_val.get("summary")
                            or raw_val.get("research")
                            or raw_val.get("description")
                            or raw_val.get("direction")
                            or raw_val.get("content")
                            or raw_val.get("text")
                            or ""
                        )
                    recents[cat] = str(raw_val)[:80].replace("\n", " ")

        lines = [
            "📚 NIBLIT KNOWLEDGE BASE SUMMARY", "",
            "📊 Storage counts:",
            f"  Facts (acquired data)  : {len(facts)}",
            f"  Events                 : {len(events)}",
            f"  Interactions           : {len(interactions)}",
            f"  Learning log entries   : {len(learning_log)}",
            f"  Pending research queue : {pending_research}",
            "", "🤖 Autonomous Learning Data (ALE) breakdown:",
        ]
        for cat, count in ale_cats.items():
            label = cat.replace("_", " ").title()
            snippet = recents.get(cat, "—")
            lines.append(f"  {label:<18}: {count} entries | latest: {snippet[:60]}")
        lines += ["", "🔖 Top tags stored:"]
        for tag, count in sorted(tag_counts.items(), key=lambda x: -x[1])[:10]:
            lines.append(f"  {tag:<30}: {count}")
        lines += [
            "", "ℹ️  Use 'recall <topic>' to query any stored fact.",
            "  Use 'acquired data [category]' to browse by category.",
        ]

        # ── Readable knowledge digest (additive, uses KnowledgeFilter) ────────
        # Show the most recent genuine research/learning facts in readable form.
        # Exclude code/evolve artifacts (generated stubs, not learned knowledge).
        _ARTIFACT_TAGS = frozenset({"evolve", "deploy", "builds", "improvement"})
        try:
            from modules.knowledge_filter import get_knowledge_filter as _gkf
            _kf = _gkf()
            # Get the 20 most recent facts that pass the knowledge filter,
            # skipping pure code-artifact entries.
            sorted_facts = sorted(facts, key=lambda x: x.get("ts", 0), reverse=True)
            knowledge_facts = [
                f for f in sorted_facts
                if not (isinstance(f, dict) and _ARTIFACT_TAGS.intersection(
                    str(t).lower() for t in (f.get("tags") or [])
                ))
            ]
            knowledge_bullets = _kf.summarize_facts(
                knowledge_facts, max_items=20, title=""
            ).splitlines()
            # Remove the empty title header and blank lines
            knowledge_bullets = [
                ln for ln in knowledge_bullets
                if ln.strip() and ln.strip() != "==="
            ]
            if knowledge_bullets:
                lines += ["", "📖 Recent knowledge (research & learning):"]
                lines += knowledge_bullets
                lines += ["  … use 'recall <topic>' for full-text search"]
        except Exception:
            pass
        # ── end of additive digest ────────────────────────────────────────────

        return "\n".join(lines)

