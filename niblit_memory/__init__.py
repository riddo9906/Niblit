def _now_iso() -> str:
    """Return current UTC time as an ISO-8601 string."""
    return datetime.now(timezone.utc).isoformat()


_NO_DATA_RE = re.compile(r"No data found for\b", re.IGNORECASE)


def _is_no_data_placeholder(text: Any) -> bool:
    """Return True when *text* is a 'No data found' placeholder.

    These placeholders are generated by SelfResearcher when all search
    backends fail and must never be stored as real knowledge, interactions,
    or learning log entries.
    """
    if not isinstance(text, str):
        return False
    return bool(_NO_DATA_RE.search(text))


