try:
    from datasets import Dataset  # noqa: F401
    _DATASETS_AVAILABLE = True
except ImportError:
    pass

try:
    from trl import SFTTrainer, DPOTrainer  # noqa: F401
    _TRL_AVAILABLE = True
except ImportError:
    pass

try:
    from peft import LoraConfig, get_peft_model, TaskType  # noqa: F401
    _PEFT_AVAILABLE = True
except ImportError:
    pass

try:
    import evaluate  # noqa: F401
    _EVALUATE_AVAILABLE = True
except ImportError:
    pass

# lm-eval-harness availability check (lazy — heavy import only inside method)
_LM_EVAL_AVAILABLE = False
