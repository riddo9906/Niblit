class LeanDeployEngine:
    """QuantConnect REST API client and live-trade deployment manager.

    All network calls use HMAC-SHA256 authentication as required by the
    QuantConnect API v2.  Methods degrade gracefully when credentials are
    missing.

    Parameters
    ----------
    knowledge_db:   Optional KnowledgeDB for storing live-trade insights.
    reflect_module: Optional ReflectModule for post-trade reflection.
    """

    def __init__(
        self,
        knowledge_db: Optional[Any] = None,
        reflect_module: Optional[Any] = None,
    ) -> None:
        self._kb = knowledge_db
        self._reflect = reflect_module
        self._monitor_threads: Dict[str, threading.Thread] = {}
        self._lock = threading.Lock()
        log.info("[LeanDeploy] Initialized — QC_USER_ID=%s",
                 "set" if self._user_id() else "NOT SET")

    # ── credentials ──────────────────────────────────────────────────────────

    def _user_id(self) -> str:
        return (
            ((_pm.get("QC_USER_ID") if _pm else None) or "").strip()
            or os.environ.get("QC_USER_ID", "").strip()
        )

    def _api_token(self) -> str:
        return (
            ((_pm.get("QC_API_TOKEN") if _pm else None) or "").strip()
            or os.environ.get("QC_API_TOKEN", "").strip()
        )

    def _auth_header(self) -> Tuple[str, str]:
        """Return (Authorization header value, timestamp string)."""
        ts = str(int(time.time()))
        token = self._api_token()
        hash_bytes = hashlib.sha256(f"{ts}:{token}".encode()).hexdigest()
        raw = f"{self._user_id()}:{hash_bytes}"
        encoded = base64.b64encode(raw.encode()).decode()
        return f"Basic {encoded}", ts

    def _headers(self) -> Dict[str, str]:
        auth, ts = self._auth_header()
        return {
            "Authorization": auth,
            "Timestamp": ts,
            "Content-Type": "application/json",
        }

    def _has_credentials(self) -> bool:
        return bool(self._user_id() and self._api_token())

    def _creds_error(self) -> str:
        return (
            "[LeanDeploy] QC_USER_ID and QC_API_TOKEN must be set.\n"
            "Get them from: https://www.quantconnect.com/account\n"
            "Set via env vars or niblit_params.json."
        )

    # ── low-level API call ───────────────────────────────────────────────────

    def _api(
        self,
        method: str,
        endpoint: str,
        payload: Optional[Dict[str, Any]] = None,
        timeout: int = 30,
    ) -> Dict[str, Any]:
        """Execute a QC REST API call.

        Returns the parsed JSON response dict, or ``{"error": "…"}`` on
        failure.
        """
        if not self._has_credentials():
            return {"error": "credentials_missing"}
        url = f"{_QC_API_BASE}/{endpoint.lstrip('/')}"
        try:
            resp = requests.request(
                method.upper(),
                url,
                headers=self._headers(),
                json=payload,
                timeout=timeout,
            )
            try:
                data = resp.json()
            except Exception:
                data = {"raw": resp.text, "status_code": resp.status_code}
            if not resp.ok:
                log.warning("[LeanDeploy] API %s %s → %s %s",
                            method, endpoint, resp.status_code, data)
            return data
        except requests.exceptions.RequestException as exc:
            log.error("[LeanDeploy] Request error %s %s: %s", method, endpoint, exc)
            return {"error": str(exc)}

    # ── status ────────────────────────────────────────────────────────────────

    def status(self) -> str:
        creds = "✅ set" if self._has_credentials() else "⚠️  not set (QC_USER_ID + QC_API_TOKEN)"
        active_monitors = sum(1 for t in self._monitor_threads.values() if t.is_alive())
        templates = list(_ALGO_TEMPLATES.keys())
        lines = [
            "=== LeanDeployEngine (QuantConnect REST API) ===",
            f"  Credentials:      {creds}",
            f"  Active monitors:  {active_monitors}",
            f"  Algorithm templates: {templates}",
            "",
            "  Required env vars:",
            "    QC_USER_ID    — QuantConnect numeric user ID",
            "    QC_API_TOKEN  — QuantConnect API token",
            "",
            "  Commands:",
            "    lean deploy status",
            "    lean deploy projects",
            "    lean deploy create <name> [<template>]",
            "    lean deploy backtest <projectId>",
            "    lean deploy live <projectId> <brokerage>",
            "    lean deploy live-list",
            "    lean deploy live-read <deployId>",
            "    lean deploy live-stop <deployId>",
            "    lean deploy liquidate <deployId>",
            "    lean deploy templates",
            "    lean deploy generate <template> <name> [symbol=X] [...]",
        ]
        return "\n".join(lines)

    # ── project management ────────────────────────────────────────────────────

    def list_projects(self) -> Any:
        """List all cloud projects for the authenticated user."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("GET", "projects/read")
        if "error" in data:
            return f"[LeanDeploy] {data['error']}"
        projects = data.get("projects", [])
        if not projects:
            return "No QC cloud projects found."
        lines = ["QC Cloud Projects:"]
        for p in projects:
            pid = p.get("projectId", "?")
            name = p.get("name", "?")
            lang = p.get("language", "?")
            modified = p.get("modified", "?")
            lines.append(f"  [{pid}] {name}  ({lang})  — modified: {modified}")
        return "\n".join(lines)

    def create_project(self, name: str, language: str = "Py") -> Any:
        """Create a new QC cloud project.

        Returns the new project ID or an error string.
        """
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("POST", "projects/create",
                         {"name": name, "language": language})
        if "error" in data:
            return f"[LeanDeploy] Create project error: {data['error']}"
        project = data.get("projects", [{}])[0]
        pid = project.get("projectId", "?")
        msg = f"✅ Created QC project '{name}' (projectId={pid})"
        log.info("[LeanDeploy] %s", msg)
        _notif_queue.push(msg)
        self._store_in_kb(f"lean_deploy:project:{pid}", msg)
        return msg

    def delete_project(self, project_id: int) -> Any:
        """Delete a QC cloud project by ID."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("POST", "projects/delete", {"projectId": project_id})
        if data.get("success"):
            return f"✅ Deleted project {project_id}"
        return f"[LeanDeploy] Delete error: {data}"

    def update_project_files(
        self,
        project_id: int,
        files: List[Dict[str, str]],
    ) -> Any:
        """Upload / overwrite source files in a cloud project.

        *files*: list of ``{"name": "main.py", "content": "…"}``.
        """
        if not self._has_credentials():
            return self._creds_error()
        results = []
        for f in files:
            r = self._api("POST", "files/create", {
                "projectId": project_id,
                "name": f["name"],
                "content": f["content"],
            })
            if not r.get("success"):
                # Try update instead of create
                r = self._api("POST", "files/update", {
                    "projectId": project_id,
                    "name": f["name"],
                    "content": f["content"],
                })
            results.append(f"{f['name']}: {'✅' if r.get('success') else '⚠️'}")
        return "Files updated: " + ", ".join(results)

    # ── compilation ───────────────────────────────────────────────────────────

    def compile_project(self, project_id: int) -> Any:
        """Compile a cloud project and return compilation results."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("POST", "compile/create", {"projectId": project_id})
        compile_id = data.get("compileId")
        if not compile_id:
            return f"[LeanDeploy] Compile failed: {data}"
        msg = f"⏳ Compiling project {project_id} (compileId={compile_id})"
        _notif_queue.push(msg)
        # Poll for completion (max 60 seconds)
        for _ in range(30):
            time.sleep(2)
            status_data = self._api("GET", f"compile/read?projectId={project_id}&compileId={compile_id}")
            state = status_data.get("state", "")
            if state == "BuildSuccess":
                ok = f"✅ Compile success: project {project_id}"
                _notif_queue.push(ok)
                return ok
            if state in ("BuildError", "error"):
                err = status_data.get("logs", str(status_data))
                return f"[LeanDeploy] Compile error: {err[:500]}"
        return f"[LeanDeploy] Compile timed out (compileId={compile_id})"

    # ── backtesting ───────────────────────────────────────────────────────────

    def create_backtest(
        self,
        project_id: int,
        compile_id: Optional[str] = None,
        name: Optional[str] = None,
    ) -> Any:
        """Launch a cloud backtest for a compiled project.

        Returns the backtest ID or error string.
        """
        if not self._has_credentials():
            return self._creds_error()
        payload: Dict[str, Any] = {"projectId": project_id}
        if compile_id:
            payload["compileId"] = compile_id
        if name:
            payload["backtestName"] = name
        data = self._api("POST", "backtests/create", payload)
        bt_id = data.get("backtestId")
        if not bt_id:
            return f"[LeanDeploy] Backtest create error: {data}"
        msg = f"⏳ Backtest started: project {project_id} (backtestId={bt_id})"
        _notif_queue.push(msg)
        return msg

    def read_backtest(self, project_id: int, backtest_id: str) -> Any:
        """Read backtest results."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("GET", f"backtests/read?projectId={project_id}&backtestId={backtest_id}")
        if "error" in data:
            return f"[LeanDeploy] {data['error']}"
        # Extract key metrics
        bt = data.get("backtest", {})
        stats = bt.get("statistics", {})
        progress = bt.get("progress", 0)
        completed = bt.get("completed", False)
        lines = [
            f"Backtest {backtest_id}:",
            f"  Progress:     {progress*100:.1f}%",
            f"  Completed:    {completed}",
        ]
        for k, v in list(stats.items())[:10]:
            lines.append(f"  {k}: {v}")
        result = "\n".join(lines)
        if completed:
            self._store_in_kb(
                f"lean_deploy:backtest:{backtest_id}",
                f"QC backtest {backtest_id} (project {project_id}): " +
                ", ".join(f"{k}={v}" for k, v in list(stats.items())[:5]),
                tags=["lean", "backtest", "quantconnect"],
            )
        return result

    def list_backtests(self, project_id: int) -> Any:
        """List all backtests for a project."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("GET", f"backtests/list?projectId={project_id}")
        backtests = data.get("backtests", [])
        if not backtests:
            return f"No backtests found for project {project_id}"
        lines = [f"Backtests for project {project_id}:"]
        for bt in backtests:
            bid = bt.get("backtestId", "?")
            name = bt.get("name", "?")
            created = bt.get("created", "?")
            completed = bt.get("completed", False)
            sharpe = bt.get("statistics", {}).get("Sharpe Ratio", "?")
            lines.append(f"  {bid} — {name}  (completed={completed}, sharpe={sharpe}, {created})")
        return "\n".join(lines)

    # ── live trading ──────────────────────────────────────────────────────────

    def create_live_algorithm(
        self,
        project_id: int,
        compile_id: str,
        brokerage: str = "PaperBrokerage",
        brokerage_settings: Optional[Dict[str, Any]] = None,
        base_live_algorithm_settings: Optional[Dict[str, Any]] = None,
        version_id: str = "-1",
    ) -> Any:
        """Deploy a compiled algorithm to live trading on QuantConnect cloud.

        Parameters
        ----------
        project_id:   Cloud project ID.
        compile_id:   Compile ID (from compile_project()).
        brokerage:    Brokerage name (e.g. ``PaperBrokerage``, ``Binance``,
                      ``OandaBrokerage``, ``InteractiveBrokersBrokerage``).
        brokerage_settings:
                      Brokerage-specific settings dict (API keys, account IDs).
        version_id:   LEAN engine version (``-1`` = latest).

        Returns str with deployment ID or error.
        """
        if not self._has_credentials():
            return self._creds_error()
        payload: Dict[str, Any] = {
            "projectId": project_id,
            "compileId": compile_id,
            "serverType": "L-MICRO",
            "baseLiveAlgorithmSettings": base_live_algorithm_settings or {},
            "versionId": version_id,
            "brokerage": {
                "id": brokerage,
                **(brokerage_settings or {}),
            },
        }
        data = self._api("POST", "live/create", payload)
        deploy_id = data.get("deployId")
        if not deploy_id:
            return f"[LeanDeploy] Live create error: {data}"
        msg = (
            f"✅ Live algorithm deployed: project={project_id} "
            f"brokerage={brokerage} deployId={deploy_id}"
        )
        log.info("[LeanDeploy] %s", msg)
        _notif_queue.push(msg)
        self._store_in_kb(
            f"lean_deploy:live:{deploy_id}",
            msg,
            tags=["lean", "live", "quantconnect"],
        )
        return msg

    def list_live_algorithms(self) -> Any:
        """List all live algorithm deployments for the authenticated user."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("GET", "live/list")
        algorithms = data.get("liveAlgorithms", [])
        if not algorithms:
            return "No live algorithms found."
        lines = ["Live Algorithms:"]
        for a in algorithms:
            did = a.get("deployId", "?")
            pid = a.get("projectId", "?")
            status = a.get("status", "?")
            launched = a.get("launched", "?")
            brokerage = a.get("brokerage", "?")
            lines.append(f"  {did} — project {pid}  status={status}  brokerage={brokerage}  launched={launched}")
        return "\n".join(lines)

    def read_live_algorithm(self, project_id: int, deploy_id: str) -> Any:
        """Read current state / statistics of a live algorithm."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("GET", f"live/read?projectId={project_id}&deployId={deploy_id}")
        if "error" in data:
            return f"[LeanDeploy] {data['error']}"
        live = data.get("liveAlgorithm", data)
        status = live.get("status", "?")
        runtime_stats = live.get("runtimeStatistics", {})
        holdings = live.get("holdings", {})
        lines = [
            f"Live Algorithm {deploy_id}:",
            f"  Status:   {status}",
        ]
        for k, v in list(runtime_stats.items())[:8]:
            lines.append(f"  {k}: {v}")
        if holdings:
            lines.append("  Holdings:")
            for sym, h in list(holdings.items())[:5]:
                lines.append(f"    {sym}: qty={h.get('quantity','?')} price={h.get('averagePrice','?')}")
        result = "\n".join(lines)
        self._store_in_kb(
            f"lean_deploy:live_status:{deploy_id}:{int(time.time())}",
            f"Live algorithm {deploy_id} status={status} stats={list(runtime_stats.items())[:4]}",
            tags=["lean", "live", "quantconnect"],
        )
        return result

    def stop_live_algorithm(self, project_id: int) -> Any:
        """Stop a running live algorithm."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("POST", "live/stop", {"projectId": project_id})
        if data.get("success"):
            msg = f"✅ Stopped live algorithm for project {project_id}"
            _notif_queue.push(msg)
            return msg
        return f"[LeanDeploy] Stop error: {data}"

    def liquidate_live_algorithm(self, project_id: int) -> Any:
        """Liquidate all open positions of a live algorithm."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("POST", "live/liquidate", {"projectId": project_id})
        if data.get("success"):
            msg = f"✅ Liquidated all positions for project {project_id}"
            _notif_queue.push(msg)
            return msg
        return f"[LeanDeploy] Liquidate error: {data}"

    # ── live order management ─────────────────────────────────────────────────

    def read_live_orders(self, project_id: int, start: int = 0, end: int = 100) -> Any:
        """Read orders placed by a live algorithm."""
        if not self._has_credentials():
            return self._creds_error()
        data = self._api("GET",
                         f"live/orders/read?projectId={project_id}&start={start}&end={end}")
        orders = data.get("orders", [])
        if not orders:
            return f"No orders for project {project_id}"
        lines = [f"Orders (project {project_id}):"]
        for o in orders:
            lines.append(
                f"  {o.get('id','?')} {o.get('symbol','?')} "
                f"qty={o.get('quantity','?')} "
                f"type={o.get('type','?')} "
                f"status={o.get('status','?')}"
            )
        return "\n".join(lines)

    # ── algorithm generation ──────────────────────────────────────────────────

    def generate_algorithm(
        self,
        template: str = "ema_crossover",
        name: str = "NiblitStrategy",
        **params: Any,
    ) -> str:
        """Generate LEAN Python algorithm source code from a template.

        Parameters
        ----------
        template:   One of: ema_crossover, crypto_ema, forex_ema, index_cfd,
                    multi_asset.
        name:       Strategy name (used as class name and description).
        **params:   Template-specific params (symbol, fast_period, etc.).

        Returns the Python source code string.
        """
        tpl = _ALGO_TEMPLATES.get(template)
        if tpl is None:
            return (
                f"[LeanDeploy] Unknown template '{template}'. "
                f"Available: {list(_ALGO_TEMPLATES.keys())}"
            )
        now = datetime.now(tz=timezone.utc)
        defaults: Dict[str, Any] = {
            "class_name": _to_class_name(name),
            "description": name,
            "symbol": params.get("symbol", "SPY"),
            "initial_cash": params.get("initial_cash", 100_000),
            "start_year": params.get("start_year", now.year - 3),
            "start_month": params.get("start_month", 1),
            "start_day": params.get("start_day", 1),
            "end_year": params.get("end_year", now.year),
            "end_month": params.get("end_month", now.month),
            "end_day": params.get("end_day", now.day),
            "fast_period": params.get("fast_period", 10),
            "slow_period": params.get("slow_period", 30),
            "momentum_period": params.get("momentum_period", 20),
            "brokerage": params.get("brokerage", "COINBASE"),
            "symbols_repr": repr(params.get("symbols", ["SPY", "QQQ", "IWM", "GLD"])),
        }
        try:
            return tpl.format(**defaults)
        except KeyError as exc:
            return f"[LeanDeploy] Template format error: {exc}"

