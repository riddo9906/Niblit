_lean_deploy_engine: Optional[LeanDeployEngine] = None


def get_lean_deploy_engine(
    knowledge_db: Optional[Any] = None,
    reflect_module: Optional[Any] = None,
) -> LeanDeployEngine:
    """Return the global :class:`LeanDeployEngine` singleton."""
    global _lean_deploy_engine
    if _lean_deploy_engine is None:
        _lean_deploy_engine = LeanDeployEngine(
            knowledge_db=knowledge_db,
            reflect_module=reflect_module,
        )
    else:
        if knowledge_db is not None and _lean_deploy_engine._kb is None:
            _lean_deploy_engine._kb = knowledge_db
        if reflect_module is not None and _lean_deploy_engine._reflect is None:
            _lean_deploy_engine._reflect = reflect_module
    return _lean_deploy_engine
