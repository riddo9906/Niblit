# Multi-asset momentum template
_TEMPLATE_MULTI_ASSET = '''\
from AlgorithmImports import *


class {class_name}(QCAlgorithm):
    """Niblit Multi-Asset Momentum — {description}"""

    SYMBOLS = {symbols_repr}

    def initialize(self):
        self.set_start_date({start_year}, {start_month}, {start_day})
        self.set_end_date({end_year}, {end_month}, {end_day})
        self.set_cash({initial_cash})
        self.indicators = {{}}
        for sym in self.SYMBOLS:
            self.add_equity(sym, Resolution.DAILY)
            self.indicators[sym] = self.momp(sym, {momentum_period}, Resolution.DAILY)

    def on_data(self, data):
        scores = {{}}
        for sym, mom in self.indicators.items():
            if mom.is_ready:
                scores[sym] = mom.current.value
        if not scores:
            return
        ranked = sorted(scores, key=scores.get, reverse=True)
        top = ranked[:max(1, len(ranked) // 3)]
        target = 1.0 / max(len(top), 1)
        for sym in self.SYMBOLS:
            if sym in top:
                self.set_holdings(sym, target)
            else:
                self.liquidate(sym)
'''

