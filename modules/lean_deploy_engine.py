#!/usr/bin/env python3
"""
modules/lean_deploy_engine.py — QuantConnect Cloud Live-Trade Deployment Engine.

Provides :class:`LeanDeployEngine`, a complete integration layer between
Niblit's trading brain and the QuantConnect REST API v2 (https://quantconnect.com/api/v2/).

This module complements the existing :class:`~modules.lean_engine.LeanEngine`
(CLI wrapper) by talking directly to the QuantConnect REST API so Niblit can:

* **Compile** algorithm source code on the QC cloud.
* **Create / list / read / stop** live trading deployments.
* **Liquidate** a live algorithm's positions.
* **Download backtest results** via REST for analysis.
* **Push algorithm updates** to existing cloud projects.
* **Self-monitor** running live algorithms and feed results back to
  Niblit's knowledge base and reflection engine.

Authentication
--------------
QuantConnect REST API v2 uses HTTP Basic Auth with HMAC-SHA256 time-stamped
token:

    Authorization: Basic base64(userId + ":" + sha256(timestamp + ":" + apiToken))

Set the following env vars (or niblit_params.json keys):

    QC_USER_ID   — QuantConnect user ID (integer string).
    QC_API_TOKEN — QuantConnect API token (generated in the dashboard).

Both are required for any cloud operation.  Without them, all methods return
descriptive error strings.

Project / Algorithm structure (QuantConnect / LEAN Python)
----------------------------------------------------------
A LEAN Python algorithm must:

1.  **Import AlgorithmImports**:
    ``from AlgorithmImports import *``

2.  **Extend QCAlgorithm** with a named class, e.g.:
    ``class MyStrategy(QCAlgorithm):``

3.  **Implement ``initialize``** — sets date range, cash, subscriptions,
    and indicators.

4.  **Implement ``on_data(data)``** — called on every new bar/tick.

5.  **Optionally** implement ``on_order_event``, ``on_end_of_algorithm``.

All algorithms generated by this module follow this exact structure and are
compatible with local ``lean backtest`` as well as cloud deployment.
"""

