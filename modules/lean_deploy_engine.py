    # ── continuous monitoring ─────────────────────────────────────────────────

    def start_monitor(
        self,
        project_id: int,
        deploy_id: str,
        interval_secs: int = _DEFAULT_MONITOR_INTERVAL_SECS,
    ) -> str:
        """Start a background thread that polls a live algorithm's status.

        Results are pushed to the notification queue and stored in KB every
        *interval_secs* seconds so Niblit's reflection engine can study them.
        """
        key = f"{project_id}:{deploy_id}"
        with self._lock:
            if key in self._monitor_threads and self._monitor_threads[key].is_alive():
                return f"⚠️  Monitor already running for deploy {deploy_id}"

        def _loop() -> None:
            log.info("[LeanDeploy] Monitor started: %s", deploy_id)
            while True:
                try:
                    result = self.read_live_algorithm(project_id, deploy_id)
                    _notif_queue.push(f"[LeanDeploy:monitor] {result[:300]}")
                    # Feed to reflect module if available
                    if self._reflect and hasattr(self._reflect, "reflect_on_trading"):
                        try:
                            self._reflect.reflect_on_lean_live(
                                deploy_id=deploy_id,
                                status_text=result,
                            )
                        except Exception:
                            pass
                except Exception as exc:
                    log.warning("[LeanDeploy:monitor] %s", exc)
                time.sleep(interval_secs)

        t = threading.Thread(
            target=_loop, daemon=True,
            name=f"LeanDeployMonitor:{deploy_id}"
        )
        t.start()
        with self._lock:
            self._monitor_threads[key] = t
        return f"✅ Monitor started for deploy {deploy_id} (interval={interval_secs}s)"

    def stop_monitor(self, deploy_id: str) -> str:
        """Stop the background monitor for a given deploy_id."""
        with self._lock:
            for k in list(self._monitor_threads.keys()):
                if deploy_id in k:
                    # Daemon threads cannot be stopped cleanly; just remove reference
                    del self._monitor_threads[k]
                    return f"✅ Monitor reference removed for {deploy_id}"
        return f"No active monitor for {deploy_id}"

    # ── quick-deploy helpers ──────────────────────────────────────────────────

    def quick_deploy(
        self,
        template: str = "ema_crossover",
        name: str = "NiblitQuickDeploy",
        brokerage: str = "PaperBrokerage",
        brokerage_settings: Optional[Dict[str, Any]] = None,
        **params: Any,
    ) -> str:
        """One-shot: generate algorithm → create project → upload → compile → deploy live.

        Returns a summary string with IDs.
        """
        if not self._has_credentials():
            return self._creds_error()

        # 1. Generate source code
        source = self.generate_algorithm(template=template, name=name, **params)
        if source.startswith("[LeanDeploy]"):
            return source

        # 2. Create cloud project
        create_resp = self._api("POST", "projects/create",
                                {"name": name, "language": "Py"})
        project_id = create_resp.get("projects", [{}])[0].get("projectId")
        if not project_id:
            return f"[LeanDeploy] Could not create project: {create_resp}"

        # 3. Upload source
        self.update_project_files(
            project_id,
            [{"name": "main.py", "content": source}],
        )

        # 4. Compile
        compile_result = self.compile_project(project_id)
        if "error" in compile_result.lower() or "BuildError" in compile_result:
            return f"[LeanDeploy] Compile failed:\n{compile_result}"

        # 5. Get compile ID (read project files to get latest compile)
        # QC API compile/create returns compileId — capture from compile_project
        # We re-run compile to capture the ID
        compile_data = self._api("POST", "compile/create", {"projectId": project_id})
        compile_id = compile_data.get("compileId", "")

        # 6. Deploy live
        deploy_result = self.create_live_algorithm(
            project_id=project_id,
            compile_id=compile_id,
            brokerage=brokerage,
            brokerage_settings=brokerage_settings,
        )
        return (
            f"Quick deploy summary:\n"
            f"  Template:    {template}\n"
            f"  Name:        {name}\n"
            f"  ProjectId:   {project_id}\n"
            f"  Brokerage:   {brokerage}\n"
            f"  Deploy:      {deploy_result}"
        )

    # ── KB helper ─────────────────────────────────────────────────────────────

    def _store_in_kb(
        self,
        key: str,
        text: str,
        tags: Optional[List[str]] = None,
    ) -> None:
        if self._kb is None:
            return
        try:
            self._kb.store(key, text, tags=tags or ["lean", "quantconnect"])
        except Exception:
            pass


